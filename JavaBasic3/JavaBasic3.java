public class JavaBasic3 {
    public static void main(String[] args) {
        int z = 5;
            for (int y = 1; y <= z; y++) {
                for (int u = 4; u >= y; u--) {
                    System.out.print(' ');
                }
                for (int t = 1; t <= y; t++) {
                    System.out.print("$");
                }
                for ( int k = 1; k <= y - k; k++){
                    System.out.print("$");
                }
                System.out.println();
            }
    }

}